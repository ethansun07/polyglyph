// ─── Helpers ──────────────────────────────────────────────────────────────────

export function getPhraseById(id) {
  return PHRASES.find(p => p.id === id) || null;
}

export function getPhrasesByCategory(category) {
  return PHRASES.filter(p => p.category === category);
}

/** All phrases whose requiredLevel is <= the highest unlocked level.
 *  Uses a simple numeric check - pair with isLevelUnlocked for strict gating. */
export function getUnlockedPhrases(highestUnlockedLevel) {
  return PHRASES.filter(p => p.requiredLevel <= highestUnlockedLevel);
}

export function getTravelEssentials(highestUnlockedLevel) {
  return PHRASES.filter(p => p.travelEssential && p.requiredLevel <= highestUnlockedLevel);
}

/** Shuffle helper */
export function shuffleArray(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}
